#This function is responsible for creating a callback that saves model checkpoints during training.
def save_checkpoint_callback(
    save_every=1,
    unit='epoch'
):
#This assertion checks if the unit parameter is either 'epoch' or 'iter'. It ensures that the provided unit is valid.
    assert unit in ('epoch', 'iter')
# It takes two parameters: model (the model instance) and loss (the loss value).
    def callback(model, loss):
        n = model.n_epochs if unit == 'epoch' else model.n_iters
 #This condition checks if the current epoch or iteration is a multiple of save_every. If true, it indicates that it's time to save a checkpoint.
        if n % save_every == 0:
            model.save(f'{run_dir}/model_{n}.pkl')
#can be used as a callback during model training to save checkpoints.
    return callback

